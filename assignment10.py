print("Welcome to the Ice Cream Parlor!")
flavour = input("Choose your ice cream flavor(chocolate, vanilla and strawberry): ")
if flavour == "chocolate":
    print("You picked chocolate! Enjoy your delicious treat.")
elif flavour == "vanilla":
    print("Vanilla is a classic choice! Enjoy your delicious treat.")
elif flavour == "strawberry":
    print("Strawberry is a fruity delight! Enjoy your delicious treat.")
else:
    print("Sorry, we don't have that flavour. Please choose from the options.")